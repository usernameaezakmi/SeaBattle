    private void rotateMoveShip(ImageView im, double move, RotateTransition rot, TranslateTransition trans) {
        if ((im.getRotate() == 90)) {
            trans.setByX(-move);
            trans.setByY(move);
            rot.setToAngle(0);
            rot.play();
            trans.play();
            //im.setX(im.getX() + move);
            //im.setY(im.getY() + move);
        }
        if ((im.getRotate() == 0)) {
            trans.setByX(move);
            trans.setByY(-move);
            rot.setToAngle(90);
            rot.play();
            trans.play();
            //im.setX(im.getX() - move);
            //im.setY(im.getY() - move);
//qq
        }
    }

